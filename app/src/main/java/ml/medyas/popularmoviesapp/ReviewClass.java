package ml.medyas.popularmoviesapp;

import android.os.Parcel;
import android.os.Parcelable;

class ReviewClass implements Parcelable {
    private String id;
    private String author;
    private String content;
    private String url;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public ReviewClass() {

    }

    public ReviewClass(String id, String author, String content, String url) {
        this.id = id;
        this.author = author;
        this.content = content;
        this.url = url;
    }

    protected ReviewClass(Parcel in) {
        id = in.readString();
        author = in.readString();
        content = in.readString();
        url = in.readString();
    }

    public static final Creator<ReviewClass> CREATOR = new Creator<ReviewClass>() {
        @Override
        public ReviewClass createFromParcel(Parcel in) {
            return new ReviewClass(in);
        }

        @Override
        public ReviewClass[] newArray(int size) {
            return new ReviewClass[size];
        }
    };

    @Override
    public int describeContents() {
        return 0;
    }

    @Override
    public void writeToParcel(Parcel parcel, int i) {
        parcel.writeString(id);
        parcel.writeString(author);
        parcel.writeString(content);
        parcel.writeString(url);
    }
}
